class dataset:
    def __init__(self,name,num_columns,num_rows,columns):
        self.name=name
        self.num_columns=num_columns
        self.num_rows=num_rows
        self.columns=columns
    def __str__(self):
        return(f"dataset {self.name} has {self.num_columns} columns and {self.num_rows} rows")
    def describe_columns(self):
        return self.columns
